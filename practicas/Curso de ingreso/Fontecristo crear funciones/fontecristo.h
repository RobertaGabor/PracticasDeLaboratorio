#ifndef FONTECRISTO_H_INCLUDED
#define FONTECRISTO_H_INCLUDED


/** \brief Se pedirán un número positivo y se mostrará la cantidad de números pares desde el número ingresado hasta el cero
 *
 * \param numero positivo
 * \return devuelve la cantidad de numeros pares entre el numero positivo y 0.
 *
 */
int numerosPares (int a);

/** \brief Se pedirán un número positivo y se mostrará la cantidad de números impares desde el número ingresado hasta el cero.
 *
 * \param numero positivo
 * \return muestra la cantidad de numeros impares desde el numero ingresado hasta 0
 *
 */
int numerosImpares (int a);

/** \brief Se pedirán un número positivo y se mostrará la cantidad de números divisibles de este número que se encuentran desde el 1 al 100.
 *
 * \param numero positivo entero
 * \return devuelve la cantidad de numeros que se pueden dividir por el numero ingresado
 *
 */
int numerosDivisible (int a);

/** \brief Se pedirán un número positivo y se mostrará si el número es un número primo o no.
 *
 * \param numero positivo entero
 * \return devuelve si es primo o no
 *
 */
void numeroPrimo (int a);

/** \brief Se pedirán un número positivo y se mostrará la cantidad de números Primos desde el número ingresado hasta el cero.
 *
 * \param numero entero positivo
 * \return devuelve la cantidad de numeros primos que hay desde 0 hasta el numero ingresado
 *
 */
int primosTotales (int a);


#endif // FONTECRISTO_H_INCLUDED
